#import "UIColor+Extensions.h"

@implementation UIColor (Extensions)

+ (UIColor *)DDAOrange {
    return [UIColor colorWithRed:0.976 green:0.675 blue:0.247 alpha:1];
}


+ (UIColor *)DDABlue {
    return [UIColor colorWithRed:0.059 green:0.416 blue:0.698 alpha:1];
}


+ (UIColor *)DDAGreen {
    return [UIColor colorWithRed:0.082 green:0.608 blue:0.310 alpha:1];
}


+ (UIColor *)DDAPaleWhite {
    return [UIColor colorWithWhite:1.0f alpha:0.4f];
}

@end
